def team_letter_lists():
    '''
    Produce a lists of teams using letters from A ...
    eg. teamlist(8) = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H']
    '''
    AsciiStart = ord('A') - 1
    for n in range(4, 25):
        team_list = []
        for i in range(1, n+1):
            team_list.append(chr(AsciiStart + i))
        print(team_list)


