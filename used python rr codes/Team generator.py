def team_number_lists():
    '''
    Produce a lists of teams using numbers from 4 to 24.
    eg. teamlist(8) = ['1', '2', '3', '4', '5', '6', '7', '8']
    ''' 
    for n in range(4, 25):
        team_list = []
        for i in range(1, n+1):
            team_list.append(str(i))
        print(team_list)


